// Char to ascii decimal / hexadecimal ascii code
#include <stdio.h>
#include <stdlib.h>

int main()
{
    char myChar1 = 'a';

    printf("Character: %c \n", myChar1);
    printf("Decimal ASCII Code: %d \n", myChar1);
    printf("Hexadecimal ASCII COde: 0x%X \n", myChar1);

    return 0;
}
